        if (resposta == JOptionPane.YES_OPTION){
        Fornecedor_DAO fornecedor_DAO = new Fornecedor_DAO();
        fornecedor_DAO.delete(ViewBean()); //bean com bean retorna bean / sbean ao quadrado
        }else{
            JOptionPane.showMessageDialog(null, "Exclus√£o cancelada", "Alerta", 2);
        }
